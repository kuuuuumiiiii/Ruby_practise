# 動物の種類によって、ターミナルに出力する鳴き方を変えるプログラム
# 'cat' : 'ニャー'
# 'dog' : 'ワン'
# 'chicken' : 'コケコッコー'
# 上記以外の場合 : '鳴き声が登録されていません'

animal = 'peple'
if animal == 'cat'
  puts 'ニャー'
elsif animal == 'dog'
  puts 'ワン'
elsif animal == 'chicken'
  puts 'コケコッコー'
else
  puts '鳴き声が登録されていません'
end